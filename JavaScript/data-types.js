//Data Types - Primitive
// number
let length = 20;
console.log(length);
length = 20.1;
console.log(length);
length = 20;
console.log(length);

// string - " " or ' '
// let greeting = 'Let's go!' - will cause an error
let greeting = "Let's go!"
console.log(greeting);

// boolean - true or false
let isStudent = true;
console.log(isStudent);

// undefined - uninitialized or missing value
let city;
console.log(city);

// null - intentional abscence of object value
let car = null;
console.log(car);